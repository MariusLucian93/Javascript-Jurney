//type of string 
const firstName = "Marius";
console.log (firstName, typeof firstName);

//type of number
const age = 100;
console.log(age, typeof age);

//type of boolean
const question = true;
console.log(question, typeof question);

//type of NULL
const aptNumber = null;
console.log(aptNumber, typeof aptNumber);

// undefined type 
let g;
console.log(g, typeof g);


//symbol type (not use very often)
const id = Symbol('id');
console.log(id, typeof id);

//type of bigint (has the n in the end, without it, its just a number)
const bigInt = 10988391009381n;
console.log(bigInt, typeof bigInt);
